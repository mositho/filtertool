import {
  chromaticItems,
  currency,
  divinationCards,
  early,
  earlySockets,
  flasks,
  gems,
  hideEquipment,
  highlightedEquipment,
  jewellery,
  joinSections,
  links,
  magicItems,
  normalItems,
  misc,
  optionalSection,
  preferredWeapons,
  questItems,
  rareItems,
  scrolls,
  showUnknownItems,
  sixSockets,
  tinctures,
  twilightStrand,
  uniques,
} from "../shared"
import { buildProfile, buildSpecificOptions } from "./config"

export const getFilter = () =>
  joinSections(
    twilightStrand(),
    currency(),
    scrolls(),
    gems(),
    links({ ...buildProfile, ...buildSpecificOptions.links }),
    highlightedEquipment(buildSpecificOptions.highlightedEquipment ?? {}),
    preferredWeapons(buildProfile),
    sixSockets(),
    jewellery(buildSpecificOptions.jewellery),
    optionalSection(buildSpecificOptions.early, (config) => early({ ...buildProfile, ...config })),
    optionalSection(buildSpecificOptions.earlySockets, (config) => earlySockets({ ...buildProfile, ...config })),
    flasks(),
    optionalSection(buildSpecificOptions.tinctures, tinctures),
    chromaticItems(),
    uniques(),
    optionalSection(buildSpecificOptions.rareItems, (config) => rareItems({ ...buildProfile, ...config })),
    optionalSection(buildSpecificOptions.magicItems, magicItems),
    optionalSection(buildSpecificOptions.normalItems, normalItems),
    questItems(),
    divinationCards(),
    misc(),
    hideEquipment(),
    showUnknownItems(),
  )
